# 雜湊 ================================================================================
# 某個東西 => 雜湊 => 加密後的東西
# 加密後的東西沒有辦法s透過任何方式還原成本來的東西
# SH256

import hashlib
sha=hashlib.sha256()
sha.update("測試".encode("utf-8"))
hash = sha.hexdigest()
print(hash)

